-- Базовые хранимые процедуры.
-- 1 процедура получает данные, остальные изменяют данные.

DROP PROCEDURE IF EXISTS get_user_results;
DROP PROCEDURE IF EXISTS add_contact_message;
DROP PROCEDURE IF EXISTS assign_test_to_user;
DROP PROCEDURE IF EXISTS update_test_result;
DROP PROCEDURE IF EXISTS change_user_group_and_role;

DELIMITER $$

-- Получить результаты прохождения тестов конкретного пользователя.
CREATE PROCEDURE get_user_results(IN p_user_id BIGINT)
BEGIN
  SELECT
    u.id AS user_id,
    u.username,
    CONCAT(u.last_name, ' ', u.first_name, ' ', u.patronymic) AS fio,
    i.name_instruction,
    t.name_test,
    r.date_instruction,
    r.result,
    r.mark,
    r.attempt
  FROM initru_res r
  JOIN initru_customuser u ON u.id = r.user_id
  JOIN initru_inst i ON i.id = r.instruction_id
  JOIN initru_test t ON t.id = r.quiz_id
  WHERE r.user_id = p_user_id
  ORDER BY r.date_instruction DESC;
END$$

-- Добавить сообщение обратной связи.
CREATE PROCEDURE add_contact_message(
  IN p_name_contact VARCHAR(125),
  IN p_email_contact VARCHAR(254),
  IN p_text_contact TEXT
)
BEGIN
  INSERT INTO initru_contact_us (name_contact, email_contact, text_contact)
  VALUES (p_name_contact, p_email_contact, p_text_contact);
END$$

-- Назначить тест пользователю.
CREATE PROCEDURE assign_test_to_user(
  IN p_user_id BIGINT,
  IN p_test_id BIGINT,
  IN p_date_target DATETIME(6)
)
BEGIN
  INSERT INTO initru_briefsforpeoples (user_id, test_id, date_target)
  VALUES (p_user_id, p_test_id, p_date_target);
END$$

-- Обновить результат прохождения теста.
CREATE PROCEDURE update_test_result(
  IN p_result_id BIGINT,
  IN p_result INT,
  IN p_mark VARCHAR(255),
  IN p_attempt INT
)
BEGIN
  UPDATE initru_res
  SET
    result = p_result,
    mark = p_mark,
    attempt = p_attempt
  WHERE id = p_result_id;
END$$

-- Изменить группу и роль пользователя.
CREATE PROCEDURE change_user_group_and_role(
  IN p_user_id BIGINT,
  IN p_group_id BIGINT,
  IN p_role_id BIGINT
)
BEGIN
  UPDATE initru_customuser
  SET
    groupStud_id = p_group_id,
    role_id = p_role_id
  WHERE id = p_user_id;
END$$

DELIMITER ;
